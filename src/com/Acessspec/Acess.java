package com.Acessspec;

public class Acess {
 
	public int a=90;
	private int b=40;
	int c=30;
	protected int d=48;
	
	public static void main(String[] args) {
		Acess obj=new Acess();
		System.out.println(obj.a);
		System.out.println(obj.b);
		System.out.println(obj.c);
		System.out.println(obj.d);
	}
	
	
	
}
